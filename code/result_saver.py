"""
结果输出模块 - 处理分析结果的保存和格式化
"""
import os
from pathlib import Path
from typing import Dict, Optional, Tuple
import pandas as pd

from config import Config


class ResultSaver:
    """结果保存器类"""
    
    def __init__(self, output_dir: Optional[str] = None):
        """
        初始化结果保存器
        
        Args:
            output_dir: 输出目录，如果不提供则使用默认配置
        """
        self.output_dir = Path(output_dir) if output_dir else Config.RESULTS_DIR
        self.output_dir.mkdir(exist_ok=True)
    
    def save_analysis_result(
        self, 
        result: Dict, 
        filename_prefix: str = "4pt_analysis", 
        article_name: str = "sample_paper"
    ) -> Tuple[Optional[str], Optional[str]]:
        """
        保存分析结果为TXT和MD格式
        
        Args:
            result: 分析结果字典
            filename_prefix: 文件名前缀
            article_name: 文章名称
            
        Returns:
            TXT和MD文件路径的元组
        """
        if not result:
            print("No result to save!")
            return None, None
        
        # 生成文件名
        timestamp = pd.Timestamp.now().strftime("%Y%m%d_%H%M%S")
        txt_filename = f"{filename_prefix}_{article_name}_{timestamp}.txt"
        md_filename = f"{filename_prefix}_{article_name}_{timestamp}.md"
        
        txt_path = self.output_dir / txt_filename
        md_path = self.output_dir / md_filename
        
        try:
            # 生成格式化内容
            txt_content = self._generate_txt_content(result)
            md_content = self._generate_md_content(result)
            
            # 保存文件
            txt_path.write_text(txt_content, encoding='utf-8')
            md_path.write_text(md_content, encoding='utf-8')
            
            print(f"\n✅ Results saved successfully:")
            print(f"   📄 TXT: {txt_path}")
            print(f"   📝 MD:  {md_path}")
            
            return str(txt_path), str(md_path)
            
        except Exception as e:
            print(f"❌ Error saving files: {e}")
            return None, None
    
    def _generate_txt_content(self, result: Dict) -> str:
        """生成TXT格式内容"""
        metadata = result['analysis_metadata']
        header = f"""4PT Framework Analysis Result
{"=" * 50}
Generated: {metadata['timestamp']}
Model: {metadata['model']}
Prompt Length: {metadata['prompt_length']} characters
Response Length: {metadata['response_length']} characters

"""
        return header + "ANALYSIS:\n" + "-" * 30 + "\n" + result['analysis_text']
    
    def _generate_md_content(self, result: Dict) -> str:
        """生成Markdown格式内容"""
        metadata = result['analysis_metadata']
        analysis_formatted = result['analysis_text'].replace('\n\n', '\n\n')
        
        return f"""# 4PT Framework Analysis Result

## 📊 Metadata
- **Generated:** {metadata['timestamp']}
- **Model:** {metadata['model']}
- **Prompt Length:** {metadata['prompt_length']:,} characters
- **Response Length:** {metadata['response_length']:,} characters

---

## 📝 Analysis Result

{analysis_formatted}

---

## 📋 Analysis Structure
The analysis follows the 4PT framework which evaluates articles based on:
1. **Sustainability fit** - Does the article fit within sustainability analyses?
2. **Problem specification** - What problems does the article address?
3. **On-ground focus** - Does it address clearly specified real-world problems?
4. **Beyond application** - Does it generate theories applicable beyond specific problems?
5. **Utility maximization** - Does it treat entities as self-interested utility maximizers?
6. **Beyond self-interest** - Does it extend beyond self-interested motivations?
7. **4PT Classification** - Final type classification (T1, T2, T3, or T4)

---
*Generated by 4PT Analysis System v2.0 - No-RAG Baseline*
"""